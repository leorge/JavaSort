echo "option   : $*" >> $0
echo "" >> $0
#
# Scan N
#
if [ $# -lt 2 ]; then
    echo "usage : $0 max_N options"
    echo "\tmax_N - The max. number of elements in power of 2"
    exit
fi

OUT=tmp.`basename $0`
./evaluate.sh $* > $OUT
expand -t `./col.awk $OUT` $OUT | tee -a $0
exit
@
